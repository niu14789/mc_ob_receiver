/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
	CRC16_Init(&__crc16);
	Flash_Init(&__flash);
  /* check nrf24l01 */	
  if( nrf24L01_Init(&__nrf,&hspi1,(unsigned int)&__flash) != 0 )
	{
		/* can not find the nrf moudle */
		HAL_GPIO_WritePin(GPIOF,GPIO_PIN_0,GPIO_PIN_SET);
		/* stop at here */
		while(1)
		{
			__nrf.state(2000);
			/* toggle */
			GPIOB->ODR ^= 1 << 1;
		}
	}  
	/* check */
  __nrf.state(1000);
	/* lighten the led */
	HAL_GPIO_WritePin(GPIOB,GPIO_PIN_1,GPIO_PIN_RESET);
	HAL_GPIO_WritePin(GPIOF,GPIO_PIN_0,GPIO_PIN_SET);	
	/* read */
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {	
		/* read data */
		if( __nrf.read(0,buffer,sizeof(buffer)) == sizeof(__rc))
		{
			/* clear the counter */
			connect_lost = 0;
			/* copy data */
			memcpy((void *)&__rc,(const void *)buffer,sizeof(__rc));
			/* check crc */
			if( __crc16.read(0,&__rc,sizeof(__rc) - 2 ) == __rc.crc && __rc.crc != 0 )
			{
				/* ok get data */
				if( ++led_freq >= 50 )
				{
					/*clear */
					led_freq = 0;
					/* toggle */
					GPIOB->ODR ^= 1 << 1;			
				}					
				/* check rc flag */
				if( __nrf.i_pri != __rc.unique_id )
				{
					/* write the new unique id into flash */
					unsigned char buffert[4];
					/* struct */
					buffert[0] = __rc.unique_id >> 8;
					buffert[1] = __rc.unique_id & 0xff;
					/* flag */
					buffert[2] = 0xAA;
					/* check sum */
					buffert[3] = (unsigned char)(buffert[0] + buffert[1]);
					/* active */
					if( __flash.write(FEBASE_ADDR,buffert,sizeof(buffert)) == 0 )
					{
						/* ok . config ok . reset the system */
						HAL_GPIO_WritePin(GPIOF,GPIO_PIN_0,GPIO_PIN_RESET);
					}
				}
				/* create dsm buffer */
				__nrf.ioctrl(CREATE_DSM,(unsigned int)dsm2_send,&__rc,sizeof(dsm2_send));
				/* end of files */
			}
		}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

