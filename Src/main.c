static unsigned int  rc_freq = 0;
/* tick process */
void tick_process(void)
{
	/* key process */
	if( __nrf.process != 0 )
	{
	   __nrf.process((unsigned int)&__flash,0,0,0,0);
	}
	/* counter */
	if( connect_lost ++ > 1000 )
	{
		/* If no data is received in a second, 
		   it is considered disconnected */
		connect_lost = 0;
		/* set led on */
		HAL_GPIO_WritePin(GPIOB,GPIO_PIN_1,GPIO_PIN_RESET);
	}
	/* send data per 20 ms */
	if( rc_freq++ >= 20 )
	{
		 /* clear and send data */
		 rc_freq = 0;
	   HAL_UART_Transmit(&huart1,dsm2_send,sizeof(dsm2_send),0xffffffff);
	}
	/* end of file */	
}
